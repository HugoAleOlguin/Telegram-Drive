const CTX = createContext<{ lang: Lang; t: (k: string, v?: Record<string,string|number>) => string; ml: (i:number) => string; setLang: (l:Lang) => void }>(null!);

export function useTranslation() { return useContext(CTX); }

export function TranslationProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    try { return (localStorage.getItem('tg-lang') as Lang) || 'en'; } catch { return 'en'; }
  });
  const set = useCallback((l: Lang) => { setLang(l); try { localStorage.setItem('tg-lang', l); } catch {} }, []);
  const t = useCallback((k: string, v?: Record<string,string|number>) => {
    let s = T[lang][k] ?? T.en[k] ?? k;
    if (v) for (const [k2, v2] of Object.entries(v)) s = s.replace(`{${k2}}`, String(v2));
    return s;
  }, [lang]);
  const ml = useCallback((i: number) => T[lang][`m${i+1}`] ?? '', [lang]);
  return <CTX.Provider value={{ lang, t, ml, setLang: set }}>{children}</CTX.Provider>;
}
